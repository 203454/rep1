

public class Caja {

    private String fruta;
    private int peso;

    public Caja(String fruta, int peso){
        this.fruta = fruta;
        this.peso = peso;

    }

    public String getFruta() {
        return fruta;
    }

    public void setFruta(String fruta) {
        this.fruta = fruta;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    
    
}
